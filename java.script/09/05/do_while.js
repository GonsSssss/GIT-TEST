do {
  console.log("무조건");
  console.log("한 번은 실행");
} while (false);

// do-while 문 - 일단 do 안의 코드블럭을 실행하고 나서 while문 조건을 살펴서 while문 조건이 틀릴때까지 반복한다.
//  일단 while 과의 차이점은 do 안의 코드블럭은 무조건 실행됨
// while문 조건을 살펴서 while문 조건이 틀릴때까지 반복
// 일반 while과의 차이점은 do안의 코드블럭은 무적권 실행됨

// 결론: do-while은 while과 거의 같은데,
// do 안의 블럭이 한번은 돌아간다는 차이있음
